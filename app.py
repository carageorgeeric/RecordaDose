import customtkinter as ctk 

ctk.set_appearance_mode("dark") 



app = ctk.CTk()
app.title("RecordaDose")
app.geometry("400x600")


MatrizRemedio = []

print("Seja Bem Vindo ao RecordaDose! ")
while True: 
    remedios = input("Digite o nome do remédio: ")
    if remedios == "":
        print("Entrada interrompida pelo usuário. ") 
        break 

    horario = input("Digite o horário(Exemplo: Meio dia e meia " \
    " ou 12:30, ou 11 da manhã... ): ")

    MatrizRemedio.append([remedios, horario])

print("\n Remédio Cadastrados: ") 
print(f"Os seus remedios são: {MatrizRemedio}")



